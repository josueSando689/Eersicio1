package prueba_2;

public class Perimetro {
	public double perimetroCuadrado (double n1, double n2, double n3, double n4 ) {
		 double suma = n1 + n2 + n3 + n4 ;
		    double promedio = suma ;
		    return promedio;
	}
	

	public double perimetroTriangulo (double n1, double n2,double n3) {
		 double suma2 = n1 + n2 +n3; 
		    double promedio2 = suma2;
		    return promedio2;	
		    
}
	}